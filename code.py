from collections import defaultdict

def count_images_by_class(directory: Path):
    """
    Подсчитывает количество изображений в каждой подпапке (классе) и общее количество.
    Возвращает словарь с количеством по классам и общее число изображений.
    """
    image_extensions = {".jpg", ".jpeg", ".png", ".bmp", ".tiff"}
    class_counts = defaultdict(int)

    # Проходим по каждой подпапке
    for class_dir in directory.iterdir():
        if class_dir.is_dir():
            count = sum(1 for file in class_dir.rglob("*") if file.suffix.lower() in image_extensions)
            class_counts[class_dir.name] = count

    total_count = sum(class_counts.values())
    return class_counts, total_count


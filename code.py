def visualize_augmentations_grid(
    image_paths,
    augmentations,
    img_size=224,
    max_samples=4,
    seed=42
):
    random.seed(seed)

    if len(image_paths) == 0:
        raise RuntimeError("Список изображений пуст.")

    sample_paths = random.sample(image_paths, k=min(max_samples, len(image_paths)))

    n_rows = len(sample_paths)
    n_cols = len(augmentations) + 1

    plt.figure(figsize=(3.5 * n_cols, 3.5 * n_rows))

    for r, img_path in enumerate(sample_paths):
        img = cv2.imread(str(img_path))
        img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)

        # ---- Original ----
        ax = plt.subplot(n_rows, n_cols, r * n_cols + 1)
        ax.imshow(img)
        ax.set_title("Original", fontsize=11)
        ax.axis("off")

        # ---- Augmentations ----
        for c, (name, aug) in enumerate(augmentations.items(), start=2):
            aug_img = aug(image=img)["image"]

            ax = plt.subplot(n_rows, n_cols, r * n_cols + c)
            ax.imshow(aug_img)
            ax.set_title(name, fontsize=11)
            ax.axis("off")

    plt.tight_layout()
    plt.show()

